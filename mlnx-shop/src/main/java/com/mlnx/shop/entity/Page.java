package com.mlnx.shop.entity;

public class Page {
	
	// 开始条数
	private int start;
	
	// 结束条数
	private int end;

	public int getStart() {
		return start;
	}

	public void setStart(int start) {
		this.start = start;
	}

	public int getEnd() {
		return end;
	}

	public void setEnd(int end) {
		this.end = end;
	}

}
